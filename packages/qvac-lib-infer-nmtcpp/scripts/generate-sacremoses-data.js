'use strict'

/**
 * Generates third-party/indic-processor-deps/sacremoses/data.js by
 * reading the upstream-synced .txt files under data/ and emitting a
 * single JS module that pernuliprops.js can `require()` directly.
 *
 * Why this exists: the previous code path used `require.asset()` +
 * `bare-fs.readFileSync()` to load Unicode-property tables and
 * nonbreaking-prefix files at runtime. That works on desktop (where
 * the .txt files sit on disk next to the code) but fails on mobile —
 * `bare-pack --linked` (used by `qvac bundle sdk`) resolves the asset
 * paths but does NOT embed the file contents into the worker.bundle's
 * virtual filesystem, so `readFileSync` throws `ENOENT` and the
 * Moses tokenizer silently falls back to empty Unicode tables.
 * That's the QVAC-16488 mobile IndicTrans regression.
 *
 * The fix: bundle the data into a plain JS module. `require()` of a
 * relative .js file is the most fundamental Bare module-resolution
 * path and works in every bundler mode. By pre-parsing here we move
 * the cost from runtime to build time.
 *
 * Run:
 *   node scripts/generate-sacremoses-data.js
 *
 * Round-trip safety: the generator is deterministic. Running it
 * twice on the same source produces byte-identical output. CI can
 * (and should) run it and assert `git diff data.js` is empty as a
 * cheap drift guard.
 *
 * Maintenance: when upstream sacremoses data is re-synced, drop the
 * new .txt files into data/ and re-run this script. Commit both
 * the .txt source and the regenerated data.js together.
 */

const fs = require('node:fs')
const path = require('node:path')

const ROOT = path.resolve(__dirname, '..')
const SACREMOSES_DIR = path.join(
  ROOT,
  'third-party',
  'indic-processor-deps',
  'sacremoses'
)
const DATA_DIR = path.join(SACREMOSES_DIR, 'data')
const OUT_FILE = path.join(SACREMOSES_DIR, 'data.js')

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

/**
 * Reads every `.txt` file under data/perluniprops/ and returns
 * a category-name → raw-string map.
 *
 * Category name is the filename without `.txt`, matching the keys
 * pernuliprops.js used in its `require.asset()` enumeration
 * (CJK, IsAlnum, IsN, etc.).
 */
function loadPerluniprops () {
  const dir = path.join(DATA_DIR, 'perluniprops')
  const out = {}
  for (const name of fs.readdirSync(dir).sort()) {
    if (!name.endsWith('.txt')) continue
    const key = name.slice(0, -'.txt'.length)
    out[key] = fs.readFileSync(path.join(dir, name), 'utf8')
  }
  return out
}

/**
 * Reads every `nonbreaking_prefix.<lang>` file under
 * data/nonbreaking_prefixes/ and returns a filename → filtered-line-
 * array map.
 *
 * Filtering matches the runtime behaviour the old `_loadFile` used
 * to do at runtime (split on \n, trim each line, drop empties and
 * lines starting with `#`). Doing it here once keeps pernuliprops.js
 * tiny and skips per-call work.
 *
 * `README.txt` lives in this directory too — explicitly skipped.
 */
function loadNonbreakingPrefixes () {
  const dir = path.join(DATA_DIR, 'nonbreaking_prefixes')
  const out = {}
  for (const name of fs.readdirSync(dir).sort()) {
    if (!name.startsWith('nonbreaking_prefix.')) continue
    const raw = fs.readFileSync(path.join(dir, name), 'utf8')
    const lines = raw
      .split('\n')
      .map((line) => line.trim())
      .filter((line) => line && !line.startsWith('#'))
    out[name] = lines
  }
  return out
}

/**
 * Renders a JS module body with header + two maps, one entry per
 * line so future diffs (e.g. when one .txt file is updated upstream)
 * are readable.
 */
function renderModule (perluniprops, nonbreakingPrefixes) {
  const lines = []
  lines.push("'use strict'")
  lines.push('')
  lines.push('// AUTO-GENERATED by scripts/generate-sacremoses-data.js — do not edit.')
  lines.push('// To refresh: drop new .txt files under sacremoses/data/, then run:')
  lines.push('//   node scripts/generate-sacremoses-data.js')
  lines.push('//')
  lines.push('// Inlined here (rather than read at runtime via `require.asset`)')
  lines.push('// because bare-pack --linked does not embed asset bytes into the')
  lines.push('// mobile worker bundle. See QVAC-16488 for the full root cause.')
  lines.push('')

  lines.push('const perluniprops = {')
  for (const key of Object.keys(perluniprops).sort()) {
    lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(perluniprops[key])},`)
  }
  lines.push('}')
  lines.push('')

  lines.push('const nonbreakingPrefixes = {')
  for (const key of Object.keys(nonbreakingPrefixes).sort()) {
    lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(nonbreakingPrefixes[key])},`)
  }
  lines.push('}')
  lines.push('')

  lines.push('module.exports = { perluniprops, nonbreakingPrefixes }')
  lines.push('')

  return lines.join('\n')
}

// ---------------------------------------------------------------------------
// Main
// ---------------------------------------------------------------------------

function main () {
  if (!fs.existsSync(DATA_DIR)) {
    console.error(`Data directory not found: ${DATA_DIR}`)
    process.exit(1)
  }

  const perluniprops = loadPerluniprops()
  const nonbreakingPrefixes = loadNonbreakingPrefixes()

  const body = renderModule(perluniprops, nonbreakingPrefixes)
  fs.writeFileSync(OUT_FILE, body)

  const totalChars = Object.values(perluniprops).reduce((s, v) => s + v.length, 0)
  const totalPrefixes = Object.values(nonbreakingPrefixes).reduce((s, v) => s + v.length, 0)
  console.log(`Wrote ${path.relative(ROOT, OUT_FILE)} (${(body.length / 1024).toFixed(1)} KB)`)
  console.log(`  perluniprops:        ${Object.keys(perluniprops).length} categories, ${totalChars.toLocaleString()} chars`)
  console.log(`  nonbreakingPrefixes: ${Object.keys(nonbreakingPrefixes).length} languages, ${totalPrefixes.toLocaleString()} prefixes`)
}

main()
